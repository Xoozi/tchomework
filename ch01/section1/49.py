#(a)画出x0附近的函数图形

def f(x):
    return (x**3 - x**2 - 5*x - 3)/((x + 1)**2)

xl = linspace(-2, -1.000001, 100)
yl = f(xl)
xr = linspace(-0.999999, 0, 100)
yr = f(xr)


xlabel('x')
ylabel('y')
plot(xl, yl, 'r-', xr, yr, 'g-')

#(b)从作图中猜测极限值
#结果是-4.0

