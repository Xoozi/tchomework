#(a)画出x0附近的函数图形

def f(x):
    return (x**4 - 16)/(x - 2)

xl = linspace(1, 1.999999, 100)
yl = f(xl)
xr = linspace(2.000001, 3, 100)
yr = f(xr)


xlabel('x')
ylabel('y')
plot(xl, yl, 'r-', xr, yr, 'g-')

#(b)从作图中猜测极限值
#结果是31.58

