'''多项式 因式分解, 求因式分解中的r1到r5,
    x**5 - x**4 - 5*x**3 = (x - r1)(x - r2)(x - r3)(x - r4)(x - r5)
    5次方程有5个根, 但观察可知, 有3个重根
    (x - 0)(x - 0)(x - 0)(x**2 - x - 5)
    r1 = r2 = r3 = 0
    剩下两根用2次方程求根公式:
    (-1 + sqrt(21))/2
    (-1 - sqrt(21))/2'''



r4 = (-1.0 + sqrt(21.0))/2.0
r5 = (-1.0 - sqrt(21.0))/2.0

