#求函数x->∞, x->-∞时的极限
#lim(x->∞)(1-12*x**3)/(4*x**2+12) = 
#lim(x->∞)(1/x**3 - 12)/(4/x+12/x**3) =
#从这里看出, 这个有理函数, 分子次数高于分母, 渐进线是个斜直线, 
#lim(x->∞)(1-12*x**3)/(4*x**2+12) = -∞
#lim(x->-∞)(1-12*x**3)/(4*x**2+12) = ∞
def f(x):
    return (1-12*x**3)/(4*x**2 + 12)

x = linspace(-1000, 1000, 1000)
y = f(x)


xlabel('x')
ylabel('y')
plot(x, y, 'r-')
