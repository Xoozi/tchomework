#极小化周长
#面积为16平方英寸的矩形最小可能的周长是什么? 它的尺寸是多少
#设边长为x和16/x
#周长p(x) = 2*x + 32/x
#周长的导数pd(x) = 2 - 32.0/x**2
#观察函数图形, 可以得知在dp/dx=0时周长取得极小值, 也是全局最小值
#2 - 32/x**2 = 0
#2 = 32/x**2
#x**2 = ± 4
#在x的定义域内只有x=4符合要求

def p(x):
    return 2*x + 32.0/x

def pd(x):
    return 2 - 32.0/(x**2)


x = linspace(1, 20, 1000)
p = p(x)
pd= pd(x)

plot([0, 100], [0, 0], '-k')
plot(x, p, '-r', x, pd, '-g')
