#内接矩形
#斜边长为2的等腰直角三角形内接矩形
#(a) 把P的坐标用x表示出来
#斜截式y = -x + 1
#(b) 把矩形的面积用x表示出来
#a(x) = 2*x * (-x + 1) = -2x**2 + 2*x
#(c) 矩形可能的最大面积
#ad(x) = -4*x + 2
#当ad(x) = 0 时a(x)取得极大值
#此时x = 0.5
#a(0.5) =  0.5

def a(x):
    return -2*x**2 + 2*x

def ad(x):
    return -4*x + 2


x = linspace(0.2, 0.8, 100)
a = a(x)
ad= ad(x)

plot([0, 1], [0, 0], '-k')
plot(x, a, '-r', x, ad, '-g')
