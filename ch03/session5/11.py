#海报尺寸设计
#需要50平方英寸的印刷内容, 以及上下底各4英寸, 左右边各2英寸的页边空白
#什么样的尺寸能使用纸最少
#设不计空白的上下边长x, 左右边长50/x
#则用纸面积a(x) = (x + 8) * (50/x + 4) = 4*x + 400/x + 82
#ad(x)的临界点是10, 在x=10处取得局部最小值, 也是全局最小值
def a(x):
    return 4*x + 400.0/x + 82

def ad(x):
    return 4 - 400.0/(x**2)

def add(x):
    return 800.0/(x**3)


x = linspace(2, 20, 1000)
y = a(x)
yd= ad(x)
ydd= add(x)

plot([0, 20], [0, 0], '-k')
plot(x, y, '-r', x, yd, '-g', x, ydd, '-b')


