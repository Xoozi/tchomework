
#不锈钢桶设计
#建造500立方英尺容量底为正方形的无盖长方体桶
#该桶是将钢板, 沿正方形底边焊接成的, 找出使得桶尽可能轻的方案
#设底边长x 则高为500/x**2
#重量和钢板面积有关
#a(x) = x*(500/x**2) * 4 + x**2 = 2000/x + x**2
#在da/dx=0时有最小值
#x = 10
def a(x):
    return 2000/x + x**2

def ad(x):
    return -2000/x**2 + 2*x

def add(x):
    return 4000/x**3 + 2


x = linspace(2, 20, 1000)
y = a(x)
yd= ad(x)
ydd= add(x)

plot([0, 20], [0, 0], '-k')
plot(x, y, '-r', x, yd, '-g')


