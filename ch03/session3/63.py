#一族三次曲线
#(a)对k = 0, 及其邻近的k的正和负值, 把f(x) = x**3 + k*x的图形画在一个公共屏幕上.
#k的值是怎么影响到图形的形状的
#k小于0时, 函数递减, 只有一个根
#k向0移动, 函数图像开始逆时针旋转, 并且开始弯曲, 靠近0时开始有多个根
#k大于0时, 又开始伸展, 
#(b)
def f(x, k):
    return x**3 + k*x


def ddd(s, e, a):
    r = 0
    g = 0
    b = 0
    k = -1280

    plot([s, e], [0, 0], '-k')

    x = linspace(s, e, a)

    while(k <= 1280):
        y = f(x, k)
        plot(x, y, '#%02X%02X%02X' % (r, g, b))
        r += 2
        k += 20

ddd(-16, 16, 1000)
