def f(x):
    return 2*x**(1.0/5.0) + 3

def fd(x):
    return 2.0/5.0*x**(-4.0/5.0)

def fdd(x):
    return -8.0/25.0*x**(-9.0/5.0)

x = linspace(-5, 5, 100)
y = f(x)
dy= fd(x)
ddy= fdd(x)

plot(x, y, '-r', x, dy, '-g', x, ddy, '-b')
