

#Glenbrook的人口为375000, 并正以每年2.25%的增长率增加， 预测何时人口达到一百万
#f(t) = 375000 * 1.0225**t
#求反函数：
#y = 375000 * 1.0225**t
#y/375000 = 1.0225**t
#log1.0225(y/375000) = t
#g(x) = ln(x/375000)/ln(1.0225)

def g(x):
    return log(x/375000)/log(1.0225)


x = linspace(300000, 1000000, 100)
y = g(x)


xlabel('x')
ylabel('y')
plot(x, y, 'r-')
